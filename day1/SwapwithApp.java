import java.util.*;
public class SwapwithApp{

    public static void main(String x[]){
              int a=10,b=20;
              int temp;
            System.out.println("before swap");
            System.out.println("A is"+a);
            System.out.println("B is"+b);
               temp =a;
                a=b;
                b=temp;
           
          System.out.println("After swap");
            System.out.println("A is"+a);
            System.out.println("B is"+b);
    }

}